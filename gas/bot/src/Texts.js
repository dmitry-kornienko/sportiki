	eventDetails(event, occupiedMain, occupiedTotal, userStatus) {
		const max = parseInt(event.maxPeople) || 0
		const dayName = getRussianDayOfWeek(event.date)
		const locText = event.location
			? `📍 [Локация на карте](${event.location})`
			: '📍 Локация: уточняется'

		let slotsText
		if (max === 0) {
			slotsText = '👥 Свободно: *Безлимит*'
		} else {
			const freeMain = max - occupiedMain
			const freeTotal = max + event.reserveLimit - occupiedTotal
			if (freeMain > 0) {
				slotsText = `👥 Свободно мест: *${freeMain} из ${max}*`
			} else if (freeTotal > 0) {
				slotsText = '👥 Основной состав заполнен — доступен *РЕЗЕРВ*'
			} else {
				slotsText = '👥 *Запись заполнена*'
			}
		}

		let note = ''
		if (userStatus.isMe || userStatus.guestCount > 0) {
			note = '✅ *Вы уже записаны.*'
		} else if (
			max > 0 &&
			occupiedMain >= max &&
			occupiedTotal < max + event.reserveLimit
		) {
			note = '⚠️ *Свободны только места в РЕЗЕРВЕ.*'
		}

		const lines = [
			`${event.type} *${escapeMarkdown(event.title)}*`,
			`📅 Дата: ${event.date} (${dayName})`,
			`⏰ Время: ${event.time}`,
			slotsText,
			note,
			'',
			escapeMarkdown(event.info),
			'',
			locText,
		]

		return lines
			.filter(
				(line, i, arr) => !(line === '' && (arr[i - 1] === '' || i === 0)),
			)
			.join('\n')
	},

