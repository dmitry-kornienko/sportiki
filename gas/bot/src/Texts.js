	reminderText(event, status) {
		let who
		if (status.isMe && status.guestCount > 0) {
			who = 'вы с гостем *будете*'
		} else if (status.isMe) {
			who = 'ты *будешь*'
		} else {
			who = 'твой гость *будет*'
		}

		return (
			'🔔 *Подтверждение участия*\n\n' +
			'Запланировано событие:\n' +
			`${event.type} *${escapeMarkdown(event.title)}*\n` +
			`📅 Дата: ${event.date}\n` +
			`⏰ Время: ${event.time}\n\n` +
			`Пожалуйста, подтверди что ${who} 👇`
		)
	},

	confirmNoQuestion(status) {
		let action
		if (status.isMe && status.guestCount > 0) {
			action =
				'Ваши записи (твоя и гостя) будут аннулированы. Подтверждаешь отмену?'
		} else if (status.isMe) {
			action = 'Твоя запись будет аннулирована. Подтверждаешь отмену?'
		} else {
			action = 'Запись гостя будет аннулирована. Подтверждаешь отмену?'
		}
		return `⚠️ *Внимание!*\n${action}`
	},
